#include<iostream>

using namespace std;


int main()
{
    int num1 , num2;
    cout<<"Enter your number 1 :\n";
    /* '<<' is called a insertion operators and use for output*/

    cin>>num1;
    /* '>>' is called a extraction operator and use for input */

    cout<<"Enter your number 2 :\n";
    /* '<<' is called a insertion operators and use for output*/

    cin>>num2;
    /* '>>' is called a extraction operator and use for input */

    cout<<"The sum of number is "<<num1+num2;
    
    return 0;
}
